    def analyze_case_data(self, case_data: Dict[str, Any]) -> Dict[str, Any]:
        """
        将案件数据转换为fill_map格式
        case_data包含被告答辩信息：
        - 答辩人信息
        - 代理人信息
        - 诉讼请求异议（每个项：无/有 + 内容）
        - 事实与理由异议（每个项：无/有 + 内容）
        - 答辩依据
        - 调解意愿
        """
        result = {}

        # T0 案号/案由
        if "案号" in case_data:
            result["案号"] = case_data["案号"]
        if "案由" in case_data:
            result["案由"] = case_data["案由"]

        # T0 答辩人信息
        defendant = case_data.get("答辩人", {})
        if defendant.get("类型") == "自然人":
            result["T0_答辩人_姓名"] = defendant.get("姓名", "")
            result["T0_答辩人_性别"] = defendant.get("性别", "")
            result["T0_答辩人_出生日期"] = defendant.get("出生日期", "")
            result["T0_答辩人_民族"] = defendant.get("民族", "")
            result["T0_答辩人_工作单位"] = defendant.get("工作单位", "")
            result["T0_答辩人_职务"] = defendant.get("职务", "")
            result["T0_答辩人_联系电话"] = defendant.get("联系电话", "")
            # 勾选
            sex = defendant.get("性别", "")
            result["T0_答辩人_性别_男"] = (sex == "男")
            result["T0_答辩人_性别_女"] = (sex == "女")
        else:
            result["T0_答辩人法人_名称"] = defendant.get("名称", "")
            result["T0_答辩人法人_住所地"] = defendant.get("住所地", "")
            result["T0_答辩人法人_注册地"] = defendant.get("注册地", "")
            result["T0_答辩人法人_法定代表人"] = defendant.get("法定代表人", "")
            result["T0_答辩人法人_法人职务"] = defendant.get("职务", "")
            result["T0_答辩人法人_联系电话"] = defendant.get("联系电话", "")

        # T1 委托代理人
        agent = case_data.get("代理人", {})
        if agent:
            result["T1_委托代理人_有"] = True
            result["T1_代理人姓名"] = agent.get("姓名", "")
            result["T1_代理人单位"] = agent.get("单位", "")
            result["T1_代理人职务"] = agent.get("职务", "")
            result["T1_代理人联系电话"] = agent.get("联系电话", "")
            agent_type = agent.get("类型", "律师")
            result["T1_委托代理人_类型_律师"] = (agent_type == "律师")
            result["T1_委托代理人_类型_亲友"] = (agent_type == "亲友")
            result["T1_委托代理人_类型_工作人员"] = (agent_type == "工作人员")
            result["T1_委托代理人_类型_基层组织推荐"] = (agent_type == "基层组织推荐")
            result["T1_委托代理人_类型_其他"] = (agent_type == "其他")

        # T1 诉讼请求异议
        objections = case_data.get("诉讼请求异议", {})
        for item, label in [
            ("本金", "本金"), ("利息", "利息"), ("提前还款", "提前还款"),
            ("担保权利", "担保权利"), ("债权费用", "债权费用"),
            ("诉讼费", "诉讼费"), ("其他请求", "其他请求"), ("标的总额", "标的总额")
        ]:
            key = item
            val = objections.get(key, {})
            has_objection = val.get("有异议", False)
            content = val.get("内容", "")
            result[f"T1_{item}_无"] = not has_objection
            result[f"T1_{item}_有"] = has_objection
            if content:
                result[f"T1_{item}_异议内容"] = content

        # T2 事实与理由异议
        fact_objections = case_data.get("事实异议", {})
        for item, label in [
            ("合同签订", "合同签订"), ("签订主体", "签订主体"),
            ("借款金额", "借款金额"), ("借款期限", "借款期限"),
            ("借款利率", "借款利率"), ("借款提供时间", "借款提供时间"),
            ("还款方式", "还款方式"), ("还款情况", "还款情况"),
            ("逾期还款", "逾期还款"), ("物的担保", "物的担保"),
            ("担保人担保物", "担保人担保物"), ("最高额担保", "最高额担保"),
            ("抵押质押登记", "抵押质押登记"), ("保证合同", "保证合同"),
            ("保证方式", "保证方式"), ("其他担保", "其他担保"),
            ("免责减责", "免责减责"), ("其他说明", "其他说明"),
        ]:
            val = fact_objections.get(item, {})
            has_objection = val.get("有异议", False)
            content = val.get("内容", "")
            result[f"T2_{item}_无"] = not has_objection
            result[f"T2_{item}_有"] = has_objection
            if content:
                result[f"T2_{item}_异议内容"] = content

        # T2 答辩依据
        basis = case_data.get("答辩依据", {})
        result["T2_答辩依据_合同"] = basis.get("合同约定", "")
        result["T2_答辩依据_法律"] = basis.get("法律规定", "")

        # T3 调解意愿
        mediation = case_data.get("调解意愿", {})
        result["T3_了解"] = mediation.get("了解调解", False)
        result["T3_不了解"] = mediation.get("不了解调解", not mediation.get("了解调解", True))
        willing = mediation.get("先行调解", "是")
        result["T3_先行调解_是"] = (willing == "是")
        result["T3_先行调解_否"] = (willing == "否")
        result["T3_先行调解_暂不确定"] = (willing == "暂不确定")

        return result

