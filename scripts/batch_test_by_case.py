def check_fill_quality(result, case_type):
    """检查填充质量，返回问题列表"""
    issues = []
    
    if not result['success']:
        issues.append(f"生成失败: {result.get('errors', [])}")
        return issues
    
    extracted = result.get('extracted_data', {})
    parties = extracted.get('parties', {})
    
    # 检查当事人信息是否提取到
    if not parties.get('plaintiff') and not parties.get('defendant'):
        issues.append("未提取到任何当事人信息")
    elif not parties.get('plaintiff'):
        issues.append("未提取到原告/答辩人/申请人信息")
    
    # 检查案由识别
    if result.get('case_type') == '未知':
        issues.append("案由识别为'未知'")
    
    # 检查文书类型识别
    if result.get('doc_type') == '民事起诉状' and '答辩' in case_type:
        issues.append(f"文书类型误判为民事起诉状（应为含答辩的类型）")
    
    # 检查输出文件
    output_path = result.get('output_path', '')
    if not output_path or not os.path.exists(output_path):
        issues.append(f"输出文件不存在: {output_path}")
    else:
        # 检查输出文件大小（空模板大约10-20KB，填充后应差不多或更大）
        file_size = os.path.getsize(output_path)
        if file_size < 5000:
            issues.append(f"输出文件过小({file_size}字节)，可能填充不完整")
    
    return issues


def run_batch_tests():
    """批量运行测试"""
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    
    generator = ElementLawsuitGenerator(
        local_template_dir=TEMPLATE_DIR,
        local_example_dir=EXAMPLE_DIR,
        output_dir=OUTPUT_DIR,
    )
    
    # 按文书类型分组统计
    results_by_type = defaultdict(list)
    all_issues = []
    total = 0
    success = 0
    partial = 0
    failed = 0
    
    for case_type, text in TEST_INPUTS.items():
        total += 1
        
        # 确定期望的文书类型
        base_case = case_type.replace('_答辩', '').replace('_行政答辩', '').replace('_第三人', '')
        
        # 写入临时文件
        tmp_file = f'/tmp/test_input_{total}.txt'
        with open(tmp_file, 'w', encoding='utf-8') as f:
            f.write(text.strip())
        
        try:
            result = generator.generate(tmp_file)
        except Exception as e:
            result = {'success': False, 'errors': [str(e)], 'case_type': '', 'doc_type': ''}
            import traceback
            traceback.print_exc()
        
        doc_type = result.get('doc_type', '未知')
        case_type_result = result.get('case_type', '未知')
        confidence = result.get('confidence', 0)
        
        issues = check_fill_quality(result, case_type)
        
        status = '✅' if result['success'] and not issues else ('⚠️' if result['success'] else '❌')
        if result['success'] and not issues:
            success += 1
        elif result['success']:
            partial += 1
        else:
            failed += 1
        
        entry = {
            'case_type': case_type,
            'detected_case': case_type_result,
            'detected_doc_type': doc_type,
            'confidence': confidence,
            'success': result['success'],
            'issues': issues,
            'status': status,
        }
        results_by_type[doc_type].append(entry)
        
        if issues:
            all_issues.append((case_type, issues))
        
        print(f"  {status} {case_type} → 案由={case_type_result} 文书类型={doc_type} 置信度={confidence:.2f}")
        if issues:
            for issue in issues:
                print(f"      ⚠ {issue}")
    
    # 汇总
    print("\n" + "=" * 70)
    print("测试汇总")
    print("=" * 70)
    print(f"总计: {total} | ✅通过: {success} | ⚠️部分: {partial} | ❌失败: {failed}")
    print()
    
    for doc_type, entries in sorted(results_by_type.items()):
        ok = sum(1 for e in entries if e['status'] == '✅')
        warn = sum(1 for e in entries if e['status'] == '⚠️')
        fail = sum(1 for e in entries if e['status'] == '❌')
        print(f"  {doc_type}: {len(entries)}个案由 → ✅{ok} ⚠️{warn} ❌{fail}")
    
    if all_issues:
        print("\n问题清单:")
        for ct, issues in all_issues:
            print(f"  【{ct}】")
            for issue in issues:
                print(f"    - {issue}")
    
    return results_by_type, all_issues


if __name__ == '__main__':
    run_batch_tests()
